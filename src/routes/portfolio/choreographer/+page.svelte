        <h3>
            Context
        </h3>
        <p>
            Well into the naughties, the standard print magazine’s web presence
            was a single static page tempting prospective subscribers with phone
            numbers to call and postal addresses to write should they wish to
            commit. No articles, no archives, no access—Business Reply Mail
            inserts by way of hypertext.
        </p>
        <p>
            <em>New York</em> was not a standard print magazine. Our first
            tranche of online verticals launched in 2006. They delivered much of
            the magazine’s writing, a passable facsimile of its editorial style,
            and digital-only features as well.
        </p>
        <p>
            Inevitably, we were also among the first publishers to bring
            subscriptions online. After three free articles, non-subscribers
            were invited to not let the paywall hit them on the way out.
            Nicknamed “Choreographer,” it had generated a small but steady
            revenue stream for years. But it was a trickle compared to
            advertising: more valuable in theory than in practice.
        </p>
        <p>
            The pandemic descended and dragged ad sales down with it, and
            suddenly all eyes were on Choreographer. The business approached
            Revenue Product with their priorities. Machine-learning metering
            based on likelihood of conversion. “Registration walls,” offering
            free articles to any reader who provided their email. Holistic
            orchestration of a reader’s entire journey through the funnel.
            Faster iteration on design and copy—A/B tested, please, while we
            were at it. And extensibility to Vox’s owned properties in addition
            to New York Media.
        </p>
        <p>
            In order to assess the 3rd generation blockchain’s ability to
            whiteboard without lessening our aptitude to repurpose without
